public class StringIntValuesClass {
    private int wierszwystopienia;
    private String wyraz;

    public StringIntValuesClass(int wierszwystopienia, String wyraz) {
        this.wierszwystopienia = wierszwystopienia;
        this.wyraz = wyraz;
    }

    public int getWierszwystopienia() {
        return wierszwystopienia;
    }

    public void setWierszwystopienia(int wierszwystopienia) {
        this.wierszwystopienia = wierszwystopienia;
    }

    public String getWyraz() {
        return wyraz;
    }

    public void setWyraz(String wyraz) {
        this.wyraz = wyraz;
    }
}
