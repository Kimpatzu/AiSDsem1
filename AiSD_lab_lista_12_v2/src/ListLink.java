public class ListLink {
    private int head;
    private int weight;

    public ListLink(int head, int weight) {
        this.head = head;
        this.weight = weight;
    }

    public int getHead() {
        return head;
    }

    public void setHead(int head) {
        this.head = head;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
